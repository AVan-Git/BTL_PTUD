	@Override
	public void actionPerformed(ActionEvent arg0) {
		Object o = arg0.getSource();

		if (o.equals(btnThoat)) {
			if (JOptionPane.showConfirmDialog(this, "Bạn có muốn thoát chương trình không?", "Cảnh báo.",
					JOptionPane.YES_NO_OPTION) == JOptionPane.YES_NO_OPTION) {
				System.exit(0);
			} else {
				XoaRongJtext();
			}

		} else if (o.equals(txtPass) || o.equals(btnDangNhap) || o.equals(txtUser)) {
			System.err.println("btnDangNhap");
			String user = txtUser.getText().trim();
			String pass = txtPass.getText().trim();

			if (user.equals("") && pass.equals("")) {
				new Timkiemsach2().setVisible(true);
				this.setVisible(false);

			} else if (check_data()) {
				System.out.println("TRUE");

				TaiKhoan a = taiKhoanDAO.getTaiKhoan_Id(user);
				if (a != null) {
					if (a.getMatKhau().equals(pass)) {
						new QL(user).setVisible(true);
						this.setVisible(false);
					} else {
						JOptionPane.showMessageDialog(this, "Sai mật khẩu!");
						txtPass.selectAll();
						txtPass.requestFocus();
					}
				} else {
					JOptionPane.showMessageDialog(this, "Tài khoản của bạn chưa được đăng ký.!");
					txtUser.selectAll();
					txtUser.requestFocus();
				}
			} else
				System.out.println("Sai: " + user + "_" + pass);
		}
	}

	private boolean check_data() {

		String user = txtUser.getText().trim();
		String pass = txtPass.getText().trim();

		if (!(user.length() > 0 && user.matches("^[a-zA-z0-9_]{4,}$"))) {

			if (user.length() == 0) {
				JOptionPane.showMessageDialog(this, "Hãy nhập tài khoản.");
			} else {
				JOptionPane.showMessageDialog(this,
						"Tên tài khoản không sử dụng ký tự đặc biệt(!@#$%^&*...). \nHãy sử dụng dấu '_' thay cho đấu cách nếu bạn cần. \nPhải trên 4 ký tự.");
			}
			txtUser.selectAll();
			txtUser.requestFocus();
			return false;
		}
		//
		if (!(pass.length() > 0 && pass.matches("^(?=.*[0-9])(?=.*[a-z])(?=.*[A-Z])(?=.*[!@#$%^&*()_+-=]).{6,20}$"))) {
			if (pass.length() == 0) {
				JOptionPane.showMessageDialog(this, "Hãy nhập mật khẩu. \nPlease");
			}
			JOptionPane.showMessageDialog(null, "Mật khẩu từ 6 đến 20 kí tự, bao gồm: \nÍt nhất 1 kí tự chữ in hoa. \n"
					+ "Ít nhất 1 kí tự chữ in thường. \nÍt nhất 1 kí tự số. \nÍt nhất 1 kí tự đặc biệt(!@#$%^&*()_+-=");
			txtPass.selectAll();
			txtPass.requestFocus();
			return false;
		}

		return true;
	}

