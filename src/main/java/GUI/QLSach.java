	private void getSuaSach() {
		int row = table.getSelectedRow();
		String ma = txtMa.getText().trim();
		String hoten = modelTable.getValueAt(row, 1).toString();
		if (JOptionPane.showConfirmDialog(this,
				"Bạn có muốn sửa thông tinh sách có mã số:' " + ma + " ' có tên :' " + hoten + " '" + " không?",
				"Cảnh báo", JOptionPane.YES_NO_OPTION) == JOptionPane.YES_NO_OPTION) {

			String ten = txtTen.getText().trim();
			double giaThanh = Double.parseDouble(txtGiaThanh.getText().trim());
			System.out.println(txtGiaThanh.getText().trim());
			System.out.println(giaThanh);
			int soLuong = Integer.parseInt(txtSoLuong.getText().trim());
//		
			String theLoai = cbbTheLoai.getSelectedItem().toString();
			String tacGiaString = cbbTacGia.getSelectedItem().toString();
			String nhaXBString = cbbNhaXB.getSelectedItem().toString();

			NhaXuatBan nhaXB = new NhaXuatBan("NXB" + nhaXBString.substring(0, 5));
			TacGia tacGia = new TacGia("TG" + tacGiaString.substring(0, 5));
			Sach a = new Sach(ma, ten, theLoai, giaThanh, soLuong, tacGia, nhaXB);

			if (sachDAO.suaSach(a)) {
				JOptionPane.showMessageDialog(this, "Cập nhật thành công.");

				xoaRongJText();

				addTable_sql(sachDAO.getDsSach());
			}
		}
	}

