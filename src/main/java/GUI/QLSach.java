	@Override
	public void actionPerformed(ActionEvent arg0) {
		Object o = arg0.getSource();
		if (o.equals(btnRefresh)) {
			xoaRongJText();
		} else if (o.equals(btnThem)) {
			if (check_data()) {
				getThemSach();
			}
		} else if (o.equals(btnSua)) {
			if (check_data()) {
				getSuaSach();
			}
		} else if (o.equals(btnXoa)) {
			if (check_data()) {
				getXoaSach();
			}
		}

	}

