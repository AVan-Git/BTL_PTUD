	private void getXoaSach() {
		String maXoa = txtMa.getText().trim();
		int row = table.getSelectedRow();
		String hoten = modelTable.getValueAt(row, 1).toString();
		
		if (JOptionPane.showConfirmDialog(this, "Bạn có muốn xóa thông tin của sách mã số:' " + maXoa+" ' có tên :' "+hoten+" '" + "Không? ", "Cảnh báo", JOptionPane.YES_NO_OPTION) == JOptionPane.YES_NO_OPTION) {
			sachDAO.xoaSach(maXoa);
			
			xoaRongJText();
			addTable_sql(sachDAO.getDsSach());
		}
	}

